//Arrays Representation
#include <iostream>
using namespace std;
int main(){
  // we can traverse through the element of arrays using for loop
  int arr[5]={1,2,3,4,5};

  for(int i=0;i<5;i++){
    cout << arr[i] << " ";  
  }
  cout << endl; //use for new line
  return 0;
}
